// --- Context ---
interface LocaleContextType {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: keyof typeof TRANSLATIONS['en-US'], defaultText?: string | { [key: string]: string | number }) => string;
}

export const LocaleContext = createContext<LocaleContextType>({
  locale: SUPPORTED_LOCALES[0],
  setLocale: () => {},
  t: (key, defaultText) => (typeof defaultText === 'string' ? defaultText : key),
});

// --- Hook ---
export const useTranslation = () => {
  return useContext(LocaleContext);
};

// --- Provider Component ---
interface LocaleProviderProps {
  children: React.ReactNode;
}

export const LocaleProvider: React.FC<LocaleProviderProps> = ({ children }) => {
  const [locale, setLocaleState] = React.useState<Locale>(() => {
    try {
        const storedLocale = localStorage.getItem('evolve-locale');
        return storedLocale ? JSON.parse(storedLocale) : SUPPORTED_LOCALES[0];
    } catch {
        return SUPPORTED_LOCALES[0];
    }
  });

  const setLocale = (newLocale: Locale) => {
    localStorage.setItem('evolve-locale', JSON.stringify(newLocale));
    setLocaleState(newLocale);
  };

  const t = (key: keyof typeof TRANSLATIONS['en-US'], defaultText?: string | { [key: string]: string | number }): string => {
    // FIX: Safely access locale code with a fallback to prevent crashes if locale is null/undefined.
    const lang = (locale?.code || SUPPORTED_LOCALES[0].code).split('-')[0];
    
    const matchingLocaleKey = Object.keys(TRANSLATIONS).find(k => k.startsWith(lang)) || 'en-US';
    const translationsForLocale = TRANSLATIONS[matchingLocaleKey] || TRANSLATIONS['en-US'];
    
    let template = translationsForLocale[key] || TRANSLATIONS['en-US'][key];
    
    if (template === undefined) {
        if (typeof defaultText === 'string') return defaultText;
        // Fallback for untranslated keys using the key itself
        const keyString = String(key);
        // Add spaces before capital letters for readability
        return keyString.replace(/([A-Z])/g, ' $1').replace(/^./, (str) => str.toUpperCase()).trim();
    }

    // FIX: Gracefully handle null interpolation object
    if (typeof defaultText === 'object' && defaultText !== null) {
        Object.entries(defaultText).forEach(([placeholder, value]) => {
            template = template.replace(`{${placeholder}}`, String(value));
            template = template.replace(`$${placeholder}`, String(value));
        });
    }

    return template;
  };
  
  const value = { locale, setLocale, t };

  return React.createElement(LocaleContext.Provider, { value: value }, children);
};